function                                                                     ...
generatePlots2D                                                              ...
        (                                                                    ...
          colors,                                                            ...
          timeSampleValues,                                                  ...
          rotationVectorsBodyCoords,                                         ...
          angularVelocitiesBodyCoords,                                       ...
          angularMomentumsBodyCoords,                                        ...
          angularVelocitiesInertialCoords,                                   ...
          angularMomentumsInertialCoords,                                    ...
          rotationalKineticEnergies,                                         ...
          demonstrationTitle                                                 ...
        )
%===============================================================================
%|
%|  FUNCTION:
%|
%|    generatePlots2D
%|
%|-----------------------------------------------------------------------------
%|
%|  PURPOSE:
%|
%|    Generate two dimensional plots of:
%|
%|      [ 1 ]  The rotation vector components in non-inertial body
%|             coordinates over time.
%|
%|      [ 2 ]  The angular velocity vector components over time in each of
%|             non-inertial body coordinates and inertial coordinates.
%|
%|      [ 3 ]  The angular momentum vector components over time in each of
%|             non-inertial body coordinates and inertial coordinates.
%|
%|      [ 4 ]  The rotational kinetic energy over time.
%|
%|-----------------------------------------------------------------------------
%|
%|  INPUTS:
%|
%|    colors
%|       A data structure containing color information.
%|       UNIT(s):   [nondimensional]
%|
%|    timeSampleValues
%|      A one dimensional vector the ith element of which is the time of the
%|      ith sample time.
%|      UNIT(s):  [seconds[
%|
%|    rotationVectorsBodyCoords
%|      A two dimensional array the ith column of which contains the
%|      three element rotation vector in non-inertial body coordinates
%|      for the ith sample time.
%|      UNIT(s):  [radians]
%|
%|    angularVelocitiesBodyCoords
%|      A two dimensional array the ith column of which contains the
%|      three element angular velocity in non-inertial body coordinates
%|      for the ith sample time.
%|      UNIT(s):  [radians/second]
%|
%|    angularMomentumsBodyCoords
%|      A two dimensional array the ith column of which contains the
%|      three element angular momentums in non-inertial body coordinates
%|      for the ith sample time.
%|      UNIT(s):  [kg meters squared / second]
%|
%|    angularVelocitiesInertialCoords
%|      A two dimensional array the ith column of which contains the
%|      three element angular velocity in inertial coordinates
%|      for the ith sample time.
%|      UNIT(s):  [radians/second]
%|
%|    angularMomentumsInertialCoords
%|      A two dimensional array the ith column of which contains the
%|      three element angular momentums in inertial coordinates
%|      for the ith sample time.
%|      UNIT(s):  [kg meters squared / second]
%|
%|    rotationalKineticEnergies
%|      A one dimensional vector the ith element of which contains the
%|      rotation kinetic energy for the ith sample time.
%|      UNIT(s):  [Joules]
%|
%|    demonstrationTitle
%|      A character string containing the title of the demonstration.
%|      UNIT(s):  [nondimensional]
%|
%|-----------------------------------------------------------------------------
%|
%|  OUTPUTS:
%|
%|    None
%|
%|-----------------------------------------------------------------------------
%|
%|  NOTE(s):
%|
%|    [ 1 ] Rotation vector definition:
%|          A rotation vector is a one dimensional row or column vector of
%|          length three.
%|            [ 1.1 ] The norm of the rotation vector is the rotation angle
%|                    in radians.
%|            [ 1.2 ] The normalized rotation vector is a 3D unit vector
%|                    which serves as the rotation axis.
%|            [ 1.3 ] Rotation vectors are identical in both non-inertial
%|                    body coordinates and inertial coordinates.
%|
%|------------------------------------------------------------------------------
%|
%|  USAGE:
%|
%|    generatePlots2D                              ...
%|            (                                    ...
%|              colors,                            ...
%|              timeSampleValues,                  ...
%|              rotationVectorsBodyCoords,         ...
%|              angularVelocitiesBodyCoords,       ...
%|              angularMomentumsBodyCoords,        ...
%|              angularVelocitiesInertialCoords,   ...
%|              angularMomentumsInertialCoords,    ...
%|              rotationalKineticEnergies,         ...
%|              demonstrationTitle                 ...
%|            );
%|
%===============================================================================

%===============================================================================
%        1         2         3         4         5         6         7         8
%2345678901234567890123456789012345678901234567890123456789012345678901234567890
%===============================================================================

%{------------------------------------------------------------------------------
   [                                                                         ...
     numberRotationVectorRows,                                               ...
     numberRotationVectorColumns                                             ...
   ] = size( rotationVectorsBodyCoords );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
   [                                                                         ...
     numberAngVelRows                                                        ...
     numberAngVelColumns                                                     ...
   ] = size( angularVelocitiesBodyCoords );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
   [                                                                         ...
     numberAngMomRows,                                                       ...
     numberAngMomColumns                                                     ...
   ] = size( angularMomentumsBodyCoords );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
   [                                                                         ...
     numberRotationKineticEnergies                                           ...
   ] = length( rotationalKineticEnergies );
%-------------------------------------------------------------------------------
   if(                                                                       ...
       ( numberRotationVectorRows == numberAngVelRows    )                   ...
       &&                                                                    ...
       ( numberAngVelRows         == numberAngMomRows    )                   ...
       &&                                                                    ...
       ( numberAngVelColumns      == numberAngMomColumns )                   ...
     )
    %{--------------------------------------------------------------------------
    %  Specified angular velocity and angular momentum of same dimension.
    %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    %  Check the number of angular velocity components.
    %---------------------------------------------------------------------------
       if(                                                                   ...
           ( numberAngVelRows    == 3 )                                      ...
           ||                                                                ...
           ( numberAngVelColumns == 3 )                                      ...
         )
        %{----------------------------------------------------------------------
        %  Specified angular velocity values are dimensionally valid.
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        %  Check the number of time samples.
        %-----------------------------------------------------------------------
           numberTimeSamples = length( timeSampleValues );
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
           numberAngularVelocitiesSpecified = max(                           ...
                                                   numberAngVelRows,         ...
                                                   numberAngVelColumns       ...
                                                 );
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
           numberAngularMomentumsSpecified  = max(                           ...
                                                   numberAngMomRows,         ...
                                                   numberAngMomColumns       ...
                                                 );
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
           numberRotationVectorsSpecified   =                                ...
                                max(                                         ...
                                     numberRotationVectorRows,               ...
                                     numberRotationVectorColumns             ...
                                   );
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
           if(                                                               ...
               ( numberAngularVelocitiesSpecified == numberTimeSamples )     ...
               &&                                                            ...
               ( numberRotationKineticEnergies    == numberTimeSamples )     ...
               &&                                                            ...
               ( numberRotationVectorsSpecified   == numberTimeSamples )     ...
             )
            %{------------------------------------------------------------------
            %  There is an angular velocity,
            %           an angular momentum,
            %           a  rotational kinetic energy
            %           and a
            %           rotation vector
            %  for each time sample.
            %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
            %  Data dimensioning appears to be consitent.
            %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
            %  Continue processing.
            %-------------------------------------------------------------------
               continueProcessing                                            ...
                       (                                                     ...
                         colors,                                             ...
                         timeSampleValues,                                   ...
                         rotationVectorsBodyCoords,                          ...
                         angularVelocitiesBodyCoords,                        ...
                         angularMomentumsBodyCoords,                         ...
                         angularVelocitiesInertialCoords,                    ...
                         angularMomentumsInertialCoords,                     ...
                         rotationalKineticEnergies,                          ...
                         demonstrationTitle                                  ...
                       );
            %}------------------------------------------------------------------
           else
            %{------------------------------------------------------------------
            %  There is not an angular velocity,
            %           not an angular momentum,
            %           not a  rotational kinetic energy
            %           OR
            %           not rotation vector
            %  for each time sample.
            %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
            %  Data dimensioning appears to be inconsitent.
            %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
            %  This is an error.
            %  Generate an error message.
            %-------------------------------------------------------------------
               STDOUT = 1;
            %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
               fprintf                                                       ...
               (                                                             ...
                 STDOUT,                                                     ...
                 [                                                           ...
                   '\n\n\n'                                                  ...
                   '%s%s\n'                                                  ...
                   '%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n'                ...
                   '%s\n%s\n%s\n'                                            ...
                   '%s%d\n'                                                  ...
                   '%s%d\n'                                                  ...
                   '%s%d\n'                                                  ...
                   '%s%d\n'                                                  ...
                   '%s%d\n'                                                  ...
                   '%s\n%s\n%s\n%s\n'                                        ...
                   '%s%s\n'                                                  ...
                   '\n\n\n'                                                  ...
                 ],                                                          ...
                 '========================================================', ...
                 '====',                                                     ...
                 '|',                                                        ...
                 '|  ERROR:',                                                ...
                 '|',                                                        ...
                 '|  There is not an angular velocity,',                     ...
                 '|           not an angular momentum,',                     ...
                 '|           not a  rotational kinetic energy',             ...
                 '|           OR',                                           ...
                 '|           not rotation vector',                          ...
                 '|  for each specified time sample value.',                 ...
                 '|',                                                        ...
                 '|    Expected as many of these vectors as there',          ...
                 '|    are time samples.',                                   ...
                 '|',                                                        ...
                 '|    Number time sample values        specified:-->',      ...
                 numberTimeSamples,                                          ...
                 '|    Number angular velocity vectors  specified:-->',      ...
                 numberAngularVelocitiesSpecified,                           ...
                 '|    Number angular momentum vectors  specified:-->',      ...
                 numberAngularMomentumsSpecified,                            ...
                 '|    Number rotation kinetic energies specified:-->',      ...
                 numberRotationKineticEnergies,                              ...
                 '|    Number rotation vectors          specified:-->',      ...
                 numberRotationVectorsSpecified,                             ...
                 '|',                                                        ...
                 '|    This is an error.',                                   ...
                 '|    Program will be terminated.',                         ...
                 '|',                                                        ...
                 '========================================================', ...
                 '===='                                                      ...
               );
            %-------------------------------------------------------------------
            %  Terminate the program.
            %-------------------------------------------------------------------
               error                                                         ...
                 (                                                           ...
                   'Input data appears to be inconsistent in dimension.'     ...
                 );
            %}------------------------------------------------------------------
           end;
        %}----------------------------------------------------------------------
       else
        %{----------------------------------------------------------------------
        %  Specified angular velocity values are not dimensionally valid.
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
        %  Generate an error message.
        %-----------------------------------------------------------------------
           STDOUT = 1;
        %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
           fprintf                                                           ...
           (                                                                 ...
             STDOUT,                                                         ...
             [                                                               ...
               '\n\n\n'                                                      ...
               '%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n'                    ...
               '%s\n%s\n'                                                    ...
               '%s%d\n'                                                      ...
               '%s\n'                                                        ...
               '%s%d\n'                                                      ...
               '%s\n%s\n%s\n%s\n%s\n'                                        ...
               '\n\n\n'                                                      ...
             ],                                                              ...
             '============================================================', ...
             '|',                                                            ...
             '|  ERROR:',                                                    ...
             '|',                                                            ...
             '|    Have encountered unexpected dimension for specified',     ...
             '|    angular velocity values.',                                ...
             '|',                                                            ...
             '|    Expected angular velocity values to be a rectangular',    ...
             '|    3 by n or n by 3 matrix.',                                ...
             '|    (Here n is the number of angular velocity values.)',      ...
             '|',                                                            ...
             '|    Number rows    in specified angular velocity',            ...
             '|    rectangular matrix is:-->',                               ...
             numberAngVelRows,                                               ...
             '|    Number columns in specified angular velocity',            ...
             '|    rectangular matrix is:-->',                               ...
             numberAngVelColumns,                                            ...
             '|',                                                            ...
             '|    This is an error.',                                       ...
             '|    Program will be terminated.',                             ...
             '|',                                                            ...
             '============================================================'  ...
           );
        %-----------------------------------------------------------------------
        %  Terminate the program.
        %-----------------------------------------------------------------------
           error                                                             ...
             (                                                               ...
               [                                                             ...
                 'Have encountered unexpected dimension for the specified',  ...
                 'angular velocity values.'                                  ...
               ]                                                             ...
             );
        %}----------------------------------------------------------------------
       end;
    %}--------------------------------------------------------------------------
   else
    %{--------------------------------------------------------------------------
    %  Specified moment of inertia values are not dimensionally valid.
    %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
    %  Generate an error message.
    %---------------------------------------------------------------------------
       STDOUT = 1;
    %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
       fprintf                                                               ...
       (                                                                     ...
         STDOUT,                                                             ...
         [                                                                   ...
           '\n\n\n'                                                          ...
           '%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n%s\n'                        ...
           '%s\n'                                                            ...
           '%s%d\n'                                                          ...
           '%s\n'                                                            ...
           '%s%d\n'                                                          ...
           '%s\n%s\n%s\n%s\n%s\n'                                            ...
           '\n\n\n'                                                          ...
         ],                                                                  ...
         '============================================================',     ...
         '|',                                                                ...
         '|  ERROR:',                                                        ...
         '|',                                                                ...
         '|    Have encountered unexpected dimension for specified',         ...
         '|    moment of inertia matrix.',                                   ...
         '|',                                                                ...
         '|    Expected a moment of inertia matrix to be a square',          ...
         '|    matrix of dimension 3.',                                      ...
         '|',                                                                ...
         '|    Number rows    in specified moment of inertia matrix',        ...
         '|    is:--->',                                                     ...
         numberInertiaMomentRows,                                            ...
         '|    Number columns in specified moment of inertia matrix',        ...
         '|    is:--->',                                                     ...
         numberInertiaMomentColumns,                                         ...
         '|',                                                                ...
         '|    This is an error.',                                           ...
         '|    Program will be terminated.',                                 ...
         '|',                                                                ...
         '============================================================'      ...
       );
    %---------------------------------------------------------------------------
    %  Terminate the program.
    %---------------------------------------------------------------------------
       error                                                                 ...
         (                                                                   ...
           [                                                                 ...
             'Have encountered unexpected dimension for the specified',      ...
             'moment of inertia matrix.'                                     ...
           ]                                                                 ...
         );
    %}--------------------------------------------------------------------------
   end;
%-------------------------------------------------------------------------------
   return;
%}------------------------------------------------------------------------------


%===============================================================================
function                                                                     ...
continueProcessing                                                           ...
        (                                                                    ...
          colors,                                                            ...
          timeSampleValues,                                                  ...
          rotationVectorsBodyCoords,                                         ...
          angularVelocitiesBodyCoords,                                       ...
          angularMomentumsBodyCoords,                                        ...
          angularVelocitiesInertialCoords,                                   ...
          angularMomentumsInertialCoords,                                    ...
          rotationalKineticEnergies,                                         ...
          demonstrationTitle                                                 ...
        )
%{------------------------------------------------------------------------------
   NO_LINE           = 'none';
   DISK_MARKER       = 'o';
%===============================================================================
   figure1Units      = 'Normalized';
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   figure1LowerLeftX = 0.0250;
   figure1LowerLeftY = 0.0000;
   figure1Width      = 0.8000;
   figure1Height     = 0.9500;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   figure1Position   = [                                                     ...
                         figure1LowerLeftX;                                  ...
                         figure1LowerLeftY;                                  ...
                         figure1Width;                                       ...
                         figure1Height                                       ...
                      ];
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   [                                                                         ...
     figure1Handle                                                           ...
   ] = figure                                                                ...
         (                                                                   ...
           'Units',         figure1Units,                                    ...
           'Position',      figure1Position                                  ...
         );
%-------------------------------------------------------------------------------
   [                                                                         ...
     numberRotationVectorsRows,                                              ...
     numberRotationVectorsColumns                                            ...
   ] = size( rotationVectorsBodyCoords );
   numberTimeSamples = numberRotationVectorsColumns;
%-------------------------------------------------------------------------------
   rotationAnglesRadians = zeros                                             ...
                            (                                                ...
                              numberTimeSamples,                             ...
                              1                                              ...
                            );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   rotationAxes          = zeros                                             ...
                            (                                                ...
                              3,                                             ...
                              numberTimeSamples                              ...
                            );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   for( timeIndex = 1 : numberTimeSamples )
     %{-------------------------------------------------------------------------
        currentRotationVector       =                                        ...
               rotationVectorsBodyCoords( :, timeIndex );
        currentRotationAngleRadians = norm( currentRotationVector );
     %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
        currentRotationAxis         = currentRotationVector        ./        ...
                                      currentRotationAngleRadians;
     %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
        rotationAnglesRadians                                                ...
                    (    timeIndex ) = currentRotationAngleRadians;
     %- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
        rotationAxes( :, timeIndex ) = currentRotationAxis;
     %}-------------------------------------------------------------------------
   end;
%-------------------------------------------------------------------------------
   numberSubPlotRows    = 4;
   numberSubPlotColumns = 1;
%-------------------------------------------------------------------------------
   currentSubPlotIndex  = 1;
   subplot                                                                   ...
      (                                                                      ...
        numberSubPlotRows,                                                   ...
        numberSubPlotColumns,                                                ...
        currentSubPlotIndex                                                  ...
      );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   yMinimum    = min( rotationAxes( 1, : ) );
   yMaximum    = max( rotationAxes( 1, : ) );
   yRange      = yMaximum - yMinimum;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   if( yRange > 0.0 )
    %{--------------------------------------------------------------------------
       yLowerLimit = yMinimum - ( 0.05 * yRange );
       yUpperLimit = yMaximum + ( 0.05 * yRange );
    %}--------------------------------------------------------------------------
   else
    %{--------------------------------------------------------------------------
       if( abs( yMinimum ) > 0.0 )
        %{----------------------------------------------------------------------
           yLowerLimit = yMinimum - ( 0.05 * abs( yMinimum ) );
           yUpperLimit = yMaximum + ( 0.05 * abs( yMaximum ) );
        %}----------------------------------------------------------------------
       else
        %{----------------------------------------------------------------------
           yLowerLimit = yMinimum;
           yUpperLimit = yMaximum;
        %}----------------------------------------------------------------------
       end;
    %}--------------------------------------------------------------------------
   end;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   if( yUpperLimit > yLowerLimit )
    %{--------------------------------------------------------------------------
       ylim                                                                  ...
        (                                                                    ...
          [                                                                  ...
            yLowerLimit,                                                     ...
            yUpperLimit                                                      ...
          ]                                                                  ...
        );
    %}--------------------------------------------------------------------------
   end;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   plot                                                                      ...
    (                                                                        ...
      timeSampleValues,                                                      ...
      rotationAxes( 1, : ),                                                  ...
      'LineStyle',            NO_LINE,                                       ...
      'Marker',               DISK_MARKER,                                   ...
      'MarkerSize',           2.0,                                           ...
      'MarkerFaceColor',      colors.Firebrick1,                             ...
      'MarkerEdgeColor',      colors.Firebrick1                              ...
    );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   [                                                                         ...
     currentAxesHandle                                                       ...
   ] = gca;
   set                                                                       ...
    (                                                                        ...
      currentAxesHandle,                                                     ...
      'FontName',            'Helvetica',                                    ...
      'FontSize',            10,                                             ...
      'FontWeight',          'Bold'                                          ...
    );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   grid on;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   [                                                                         ...
     plotTitleString                                                         ...
   ] = sprintf                                                               ...
        (                                                                    ...
          '%s\n%s    %s\n',                                                  ...
          demonstrationTitle,                                                ...
          'ROTATION VECTOR COMPONENTS',                                      ...
          'NON-INERTIAL BODY COORDINATES'                                    ...
        );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   [                                                                         ...
     yLabelString                                                            ...
   ] = sprintf                                                               ...
        (                                                                    ...
          '%s\n%s\n%s',                                                      ...
          'ROTATION AXIS',                                                   ...
          'X COMPONENT',                                                     ...
          '[nondimensional]'                                                 ...
        );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   titleHandle =                                                             ...
   title                                                                     ...
    (                                                                        ...
      plotTitleString,                                                       ...
      'FontSize',           24,                                              ...
      'FontName',           'Helvetica',                                     ...
      'FontWeight',         'Bold'                                           ...
    );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   yLabelHandle =                                                            ...
   ylabel                                                                    ...
    (                                                                        ...
      yLabelString,                                                          ...
      'FontSize',           12,                                              ...
      'FontName',           'Helvetica',                                     ...
      'FontWeight',         'Bold'                                           ...
    );
%-------------------------------------------------------------------------------
   currentSubPlotIndex  = 2;
   subplot                                                                   ...
      (                                                                      ...
        numberSubPlotRows,                                                   ...
        numberSubPlotColumns,                                                ...
        currentSubPlotIndex                                                  ...
      );
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   yMinimum    = min( rotationAxes( 2, : ) );
   yMaximum    = max( rotationAxes( 2, : ) );
   yRange      = yMaximum - yMinimum;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   if( yRange > 0.0 )
    %{--------------------------------------------------------------------------
       yLowerLimit = yMinimum - ( 0.05 * yRange );
       yUpperLimit = yMaximum + ( 0.05 * yRange );
    %}--------------------------------------------------------------------------
   else
    %{--------------------------------------------------------------------------
       if( abs( yMinimum ) > 0.0 )
        %{----------------------------------------------------------------------
           yLowerLimit = yMinimum - ( 0.05 * abs( yMinimum ) );
           yUpperLimit = yMaximum + ( 0.05 * abs( yMaximum ) );
        %}----------------------------------------------------------------------
       else
        %{----------------------------------------------------------------------
           yLowerLimit = yMinimum;
           yUpperLimit = yMaximum;
        %}----------------------------------------------------------------------
       end;
    %}--------------------------------------------------------------------------
   end;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   if( yUpperLimit > yLowerLimit )
    %{--------------------------------------------------------------------------
       ylim                                                                  ...
        (                                                                    ...
          [                                                                  ...
            yLowerLimit,                                                     ...
            yUpperLimit                                                      ...
          ]                                                                  ...
        );
    %}--------------------------------------------------------------------------
   end;
%- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - 
   plot                                                                      ...
